    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        // TODO add your handling code here:
        try{
            this.fornec = getFornecedor();
        }
        catch(ErroValidacaoException ex){
            JOptionPane.showMessageDialog(null, "Erro de validação: " + ex.getMessage());
            return;
        }
        //mostra msg de confirmação
        if (JOptionPane.showConfirmDialog(null, "Deseja realmente alterar o registro?",
            "Confirmar", JOptionPane.YES_NO_OPTION) == 0){
        //clicou sim
        if (this.repo_fornec.Salvar(fornec)){
            JOptionPane.showMessageDialog(null, "Dados salvos com sucesso!");
            dispose();
        }else{
            JOptionPane.showMessageDialog(null, "Falha ao salvar os dados! Consulte o administrador do banco de dados");
            dispose();
        }
        }
        //clicou em não
        else{
            JOptionPane.showMessageDialog(null, "Cancelado com sucesso!");
        }

    }//GEN-LAST:event_btnSalvarActionPerformed

    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        // TODO add your handling code here:
        fornec = new Fornecedor();
        if(txtId.getText().length() > 0){
            fornec.setId(Integer.parseInt( txtId.getText() ));
            try {
                fornec = repo_fornec.Abrir(fornec.getId());
                if(fornec != null){
                    // preenchimento automático
                    this.txtCnpj.setText(fornec.getCnpj());
                    this.txtRazaoSocial.setText(fornec.getRazaoSocial());
                    this.txtEndCompleto.setText(fornec.getEndCompleto());
                    this.cbxStatus.setSelectedItem(fornec.getStatus().name());
                    //exibe telefones e emails cadastrados
                    atualizarTelefones();
                    atualizarEmails();
                                     
                } else
                    JOptionPane.showMessageDialog(null, "Fornecedor Inexistente!");               
            } catch (ErroValidacaoException ex) {
                Logger.getLogger(EditarFornecedor.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_btnBuscarActionPerformed


