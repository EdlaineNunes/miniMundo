    public void setFornecedor(Fornecedor fornec){
        this.fornec = fornec;
        txtCnpj.setText(fornec.getCnpj());
        txtRazaoSocial.setText(fornec.getRazaoSocial());
        txtEndCompleto.setText(fornec.getEndCompleto());
        cbxStatus.setSelectedItem(fornec.getStatus().name());
        
        atualizarTelefones();
        atualizarEmails();
         
     }
    
     public Fornecedor getFornecedor() throws ErroValidacaoException{
        this.fornec.setCnpj(txtCnpj.getText());
        this.fornec.setRazaoSocial(txtRazaoSocial.getText());
        this.fornec.setEndCompleto(txtEndCompleto.getText());
        this.fornec.setStatus(Status.valueOf(cbxStatus.getSelectedItem().toString()));
        
        atualizarTelefones();
        atualizarEmails();
        
        return this.fornec;
     
     }
     
     private void atualizarTelefones(){
        String[] tel = new String[fornec.getTelefones().size()];
        tel = fornec.getTelefones().toArray(tel);
        ListModel<String> telefones = new DefaultComboBoxModel<>(tel) ;
        listTelefone.setModel(telefones);
     }
    
     private void atualizarEmails(){
        String[] email = new String[fornec.getEmail().size()];
        email = fornec.getEmail().toArray(email);
        ListModel<String> emails = new DefaultComboBoxModel<>(email) ;
        listEmail.setModel(emails);
     }
