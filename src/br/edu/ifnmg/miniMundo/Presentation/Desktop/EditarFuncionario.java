    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        // TODO add your handling code here:
        pessoa = new Pessoa();
        func = new Funcionario();
        if(txtId.getText().length() > 0){
            func.setId(Integer.parseInt( txtId.getText() ));
            try {
                func = repo_func.ValidarId(func);
                if(func != null){
                    pessoa = repo_pessoa.PessoaId(func);
                    // preenchimento automático
                    this.txtNome.setText(pessoa.getNome());
                    this.txtCPF.setText(pessoa.getCpf());
                    this.cbxSexo.setSelectedItem(pessoa.getSexo().name());
                    this.txtUser.setText(func.getUser());
                    this.txtSenha.setText(func.getSenha());
                    this.cbxStatus.setSelectedItem(func.getStatus().name());
                    //exibe telefones e emails cadastrados
                    atualizarTelefones();
                } else
                JOptionPane.showMessageDialog(null, "Funcionário Inexistente!");
            } catch (ErroValidacaoException ex) {
                Logger.getLogger(EditarFuncionario.class.getName()).log(Level.SEVERE, null, ex);
            } catch (SQLException ex) {
                Logger.getLogger(EditarFuncionario.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_btnBuscarActionPerformed

